<h4>I . Why do you need to be baptized?</h4>
<ul>
	<li>1. According to the Bible, getting baptized is a formal expression of our internal belief.</li>
	<li>2. Per Jesus, all believers have to be baptized. (Matt 28: 19-20)</li>
	<li>3. We need to follow Jesus’ examples. (Mark 1:9 )</li>
</ul>
<h4>II. What is the significance of baptism?</h4>
<ul>
	<li>1. Baptism is a mechanism used to express our acknowledgment of Jesus’ death, being buried, and
		resurrection. (I Cor 15:3-4; Col 2:12)</li>
	<li>2. Baptism is also an expression of our born again life after our commitment to the Lord. (II Cor 5:17;
		Roman6:4)</li>
</ul>
<h4>III. Who needs to be baptized?</h4>
<ul>
	<li>1. Anyone who has decided to believe in Christ. (Act 2s: 41; 8: 12-13)</li>
	<li>2. We do not baptize infants.</li>
</ul>
<h4>IV. When do we need to be baptized?</h4>
<ul>
	<li>1. At the moment, we have decided to believe in Christ. (Acts 2: 41)</li>
	<li>2. You have to be baptized immediately in order to witness to others that you are a Christian.</li>
</ul>
<h4>V. What is the baptism ceremony in Crosspoint?</h4>
<ul>
	<li>1. Similar to Jesus’ example, the whole body has to be emerged to the water. (Matt 3:16; Acts 8: 38-39)</li>
	<li>2. Please sign up for baptism.</li>
	<li>3. On the day of the baptism ceremony, Crosspoint will provide you gowns. You need to prepare other
		personal belongings including towels, clothings, and dryer.</li>
</ul>
<h4>Conclusion:</h4>
<p>
	Being baptized does not make you a Christian. However, baptism is an expression that you have
	become a Christian. You will not be saved through the baptism ceremony. You can be only saved when
	you accept Christ as your personal savior. Baptism ceremony is like a wedding ring which indicates your
	internal expression of your religious belief.</p>
